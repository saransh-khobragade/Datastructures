//max
console.log(Math.max(5, 6)); //6

//min
console.log(Math.min(5, 6)); //5

//floor
console.log(Math.floor(3.1)); //3

//ceiling
console.log(Math.ceil(3.1)); //4

//rounding
console.log(Math.round(6.1)); //6
console.log(Math.round(6.5)); //7
console.log(Math.round(6.6)); //7

//round upto 2 decimal
num = 1.2233638;
console.log(num.toFixed(3)); //1.223

// Random upto 10
console.log(Math.floor(Math.random() * 10)); //5

//Power
console.log(Math.pow(5, 2)); //25

//Sqaure root
console.log(Math.sqrt(25)); //5
