#Arithmetic operators
a = 10
b = 5
print(a + b) #15
print(a - b) #5
print(a * b) #50
print(a / b) #2.0
print(a % b) #0     #mod
print(a ** b) #100000

#Assignment operators
a = 10
b = 5
a += b #a = a + b
print(a) #15
a -= b #a = a - b
print(a) #10
a *= b #a = a * b
print(a) #50
a /= b #a = a / b
print(a) #2.0

#Comparison operators
a = 10
b = 5
print(a == b) #False
print(a != b) #True
print(a > b) #True
print(a < b) #False
print(a >= b) #True
print(a <= b) #False

#Logical operators
a = True
b = False
print(a and b) #False
print(a or b) #True
print(not a) #False

#Identity operators

a = 10
b = 5
print(a is b) #False
print(a is not b) #True

#Membership operators
a = 10
b = 5
print(a in b) #False
print(a not in b) #True
